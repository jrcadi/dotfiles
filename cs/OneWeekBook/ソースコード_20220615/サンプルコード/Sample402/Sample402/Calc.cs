using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Sample402
{
    class Calc
    {
        //  2つの整数の引数の和を求める
        public int Add(int a, int b)
        {
            return a + b;
        }
        //  3つの整数の引数の和を求める
        public int Add(int a, int b, int c)
        {
            return a + b + c;
        }
    }
}
